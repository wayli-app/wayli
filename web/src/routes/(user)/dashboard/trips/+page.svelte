					'Content-Type': 'application/json'
				},
				body: JSON.stringify({
					startDate: tripForm.start_date,
					endDate: tripForm.end_date
				})
			});

			if (!response.ok) {
				throw new Error('Failed to suggest image');
			}

			const result = await response.json();
