														: label === 'adventure'
															? 'bg-orange-100 text-orange-700 dark:bg-orange-900/30 dark:text-orange-300'
															: 'bg-gray-100 text-gray-700 dark:bg-gray-700 dark:text-gray-300'}"
										>
											{label}
										</span>
									{/each}
								</div>
							{/if}
