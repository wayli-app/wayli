												class="grid grid-cols-2 gap-4 text-sm text-gray-600 md:grid-cols-4 dark:text-gray-400"
											>
												<div>
													<span class="font-medium">Dates:</span><br />
													{format(new Date(trip.start_date), 'MMM d')} - {format(
														new Date(trip.end_date),
														'MMM d, yyyy'
